// globales
parseInt("42"); // 42
parseFloat("3.14"); // 3.14
isNaN("abc"); // true
isFinite(100); // true
eval("2 + 2"); // 4

// temporizadores
setTimeout(() => console.log("Hola"), 1000);
setInterval(() => console.log("Cada segundo"), 1000);

// math
Math.max(1, 5, 2); // 5
Math.random(); // entre 0 y 1
Math.floor(2.8); // 2
Math.round(2.5); // 3

// otras utilidades
alert("Hola"); // muestra un cuadro de alerta (solo navegador)
prompt("Tu nombre:"); // solicita entrada del usuario
console.log("Debug"); // imprime en consola
